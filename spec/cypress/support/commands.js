Cypress.Commands.add('loginWithGoogle', () => {
    cy.visit('/');
    cy.get('.login').should('be.visible').click();
    cy.url().should('include', 'accounts.google.com', { timeout: 10000 });
    cy.url().should('include', '/questions/start');
});
